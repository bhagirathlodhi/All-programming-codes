
public class Boxing {
	public static void main(String[] args) {
		int a=10;
		Integer a1=new Integer(a);// Autoboxing
		int a2 =a1;// Unboxing
		Integer a3=90;// Autoboxing
		int a4=a3;// Unboxing
		System.out.println(a);
		System.out.println(a1);
		System.out.println(a2);
		System.out.println(a3);
		System.out.println(a4);
		
	}

}
